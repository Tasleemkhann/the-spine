                <div id="progress-content-section">
                    <div class="section-content discovery active">
                        <h2>The Beginning of A Hopeful Future</h2>
                        <p>The absence of basic spine care in rural India was the sharp realisation that led to the
                            founding of The Spine Foundation on May 19, 1998. Dr. Shekhar Bhojraj saw this suffering and
                            imagined a different outcome. He envisioned an India where no one's right to spinal
                            well-being would be denied and where access to this crucial treatment was not limited by
                            distance.</p>
                    </div>

                    <div class="section-content strategy">
                        <h2>Journey Through Hardships</h2>
                        <p>With the opening of its first OPD at SEARCH Hospital in Gadchiroli, the Spine Foundation's
                            goal came into effect. Upon becoming aware of the harsh reality of tribal life, the TSF team
                            came across a community that was plagued by back pain. Harsh living conditions added to
                            these challenges, which frequently resulted from spinal issues and had an impact on
                            livelihoods and overall well-being. To address this underprivileged population's demand for
                            immediate relief and spinal healthcare, TSF founded the OPD.</p>
                    </div>

                    <div class="section-content creative">
                        <h2>A New Point of View: Spinecare For All</h2>
                        <p>The urban poor in Mumbai endure an unseen battle behind the gleaming appearance of their
                            fast-paced lifestyle. Many struggle with the same back problems as those in rural areas and
                            are unable to afford appropriate care. The Spine Foundation recognised it and built a
                            special spine unit at Mumbai's V.N. Desai Hospital. This important programme makes
                            high-quality, easily accessible healthcare available to these underprivileged communities,
                            preventing financial limitations from affecting spinal health.</p>
                    </div>

                    <div class="section-content production">
                        <h2>The Reward of Dedication</h2>
                        <p>The Spine Foundation's journey has evolved from a single outpatient procedure centre in
                            Gadchiroli to a landmark of 100 life-changing surgeries. This accomplishment is strong
                            evidence of their unwavering passion and dedication to changing healthcare access. TSF has
                            not only strengthened the community's spines but also their spirit by raising awareness and
                            providing essential care in this harsh region, allowing them to not just survive but truly
                            thrive.</p>
                    </div>

                    <div class="section-content analysis">
                        <h2>Focusing on Education and Kindness</h2>
                        <p>TSF provides life-changing spine care to people who need it most by organising spine camps
                            and workshops in more than 20 regions of India. TSF cultivates a cooperative network by
                            collaborating with committed Asha workers and doctors in the community. This method not only
                            provides quick relief but also equips the community at large with the information and tools
                            needed to create a long-term, sustainable foundation for spinal health.</p>
                    </div>
                </div>
            </div>
